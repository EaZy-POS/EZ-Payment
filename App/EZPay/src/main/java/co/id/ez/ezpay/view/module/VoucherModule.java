    @Override
    public void search() {
        product = null;
        if (txt_tujuan.getText().length() < 4) {
            Common.showWarningMessage("Mohon isi nomor tujuan dengan benar!", this);
            txt_tujuan.requestFocus();
        } else {
            if (txt_voucher_id.getText().length() >= 3) {
                getVoucherID();
            } else {
                popupMenu.hide();
            }
        }
    }

    @Override
    public void loadData() {
        try {
            txt_voucher_id.setText("");
            dataHolderList.clear();
            popupMenu.clear();
            String tProductType = "VCR";
            
            if (rd_voucher.isSelected()) {
                tProductType = "VCR";
            }
            
            if (rd_emoney.isSelected()) {
                tProductType = "EMY";
            }
            
            if (rd_ewallet.isSelected()) {
                tProductType = "EWL";
            }
            
            if (rd_paketdata.isSelected()) {
                tProductType = "PDT";
            }
            
            LinkedList<JSONObject> data = transactionController.getVoucherProductList(tProductType);
            if (data != null && data.size() > 0) {
                data.forEach(dataVoucher -> {
                    String tPrefix = dataVoucher.get("prefix").toString();
                    String tVoucher = dataVoucher.get("voucher_id").toString();
                    String tVoucherName = dataVoucher.get("voucher_name").toString();
                    String tProvider = dataVoucher.get("provider_name").toString();
                    BigDecimal tPrice = new BigDecimal(dataVoucher.getDouble("sale_price"));
                    VoucherModel model = new VoucherModel(tVoucher, tVoucherName, tProvider, tPrefix, tPrice);
                    dataHolderList.putData(model);
                });

            }
        } catch (SQLException e) {
            Common.showErrorMessage("Gagal mencari data produk voucher", this);
            Common.errorLog("[SQLException] Error on get voucher product", e);
        }
    }

    private void getVoucherID() {
        String tVoucherID = txt_voucher_id.getText().trim();
        String tPrefix = txt_tujuan.getText().trim().substring(0, 4);
        popupMenu.show(dataHolderList, tPrefix + "#" + tVoucherID);
        
        if(popupMenu.isEmpty()){
            popupMenu.show(dataHolderList, tVoucherID);
        }

    }

    @Override
    public void performInquiry() {
        try {
            VoucherInquiry request = new VoucherInquiry();
            request.setTujuan(txt_tujuan.getText());
            request.setVoucherid(product.getVoucher_id());
            billerResponse = sendRequest(request);

            if (billerResponse != null) {
                saveTransaction();
                JSONObject tPayload = billerResponse.getPayload();
                txt_voucher.setText(product.getVoucher_name());
                txt_nominal.setText(tPayload.get("nominal").toString());
                txt_harga.setText(product.getPrice_sale().toPlainString());
                Common.activateComponent(true, btn_bayar, btn_batal);
                Common.activateComponent(false, btn_cek, btn_reload, txt_tujuan, txt_voucher_id);
            } else {
                txt_voucher_id.requestFocus();
            }
            
        } catch (JSONException | SQLException e) {
            Common.errorLog("[JSONException | SQLException] Failed to execute Perform voucher inquiry", e);
            Common.showErrorMessage(MessageType.SYSTEM_ERROR, this);
        }catch (Exception e) {
            Common.errorLog("[Exception] Failed to execute Perform voucher inquiry", e);
            Common.showErrorMessage(MessageType.FATAL_ERROR, this);
        }
    }

    @Override
    public void performPayment() {
        try {
            prePostPayment("trx_vcr_sales", billerResponse);
            
            VoucherPayment request = new VoucherPayment(billerResponse, product);
            billerResponse = sendRequest(request);

            if (billerResponse != null) {
                if(billerResponse.getResponseCode() == RC.SUCCESS){
                    updateDataTransaction();
                }
            } else {
                transactionController.updateFailedTransaction("trx_vcr_sales", request);
                resetForm();
            }
        } catch (SQLException e) {
            Common.errorLog("[SQLException] Failed to execute Perform voucher payment", e);
            Common.showErrorMessage(MessageType.SYSTEM_ERROR, this);
            resetForm();
        }catch (Exception e) {
            Common.errorLog("[Exception] Failed to execute Perform voucher payment", e);
            Common.showErrorMessage(MessageType.FATAL_ERROR, this);
            resetForm();
        }
    }

    @Override
    public boolean validateForm() {
        if(product == null){
            product = (VoucherModel) popupMenu.getSelectedModel();
        }
        
        return txt_tujuan.getText().length() > 0
                && txt_voucher_id.getText().length() > 0
                && product != null;
    }

    @Override
    public void updateDataTransaction() {
        try {
            JSONObject resp = billerResponse.getPayload();
            transactionController.updateSuccessTransactionVoucher(resp);
            Common.showInfoMessage(resp.getString("rcm"), this);
            previewStruk(resp.getString("trxid"), "trx_vcr_sales");
            resetForm();
        } catch (JSONException | SQLException e) {
            Common.showErrorMessage(MessageType.SYSTEM_ERROR, this);
            Common.errorLog("[SQLException] Error on get voucher product", e);
            resetForm();
        }
    }

    @Override
    public QueryBuilder contructQueryInsertTransaction() {
        JSONObject resp = billerResponse.getPayload();
        
        QueryBuilder builder = new QueryBuilder("trx_vcr_sales", QueryType.INSERT);
        builder.addEntryValue("id", "UUID()", true);
        builder.addEntryValue("transaction_date", "NOW()", true);
        builder.addEntryValue("user_id", App.environment().getDEU());
        builder.addEntryValue("dest_number", resp.getString("tujuan"));
        builder.addEntryValue("voucher_id", resp.getString("voucherid"));
        builder.addEntryValue("nominal", resp.get("nominal").toString());
        builder.addEntryValue("harga", resp.get("harga").toString());
        builder.addEntryValue("harga_jual", product.getPrice_sale().toPlainString());
        builder.addEntryValue("refnumber", resp.get("refnum").toString());
        builder.addEntryValue("transaction_id", resp.get("trxid").toString());
        
        return builder;
    }

