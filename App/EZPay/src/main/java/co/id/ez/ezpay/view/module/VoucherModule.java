    private void txt_voucher_idKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_voucher_idKeyPressed
        // TODO add your handling code here:
        switch (evt.getKeyCode()) {
            case KeyEvent.VK_UP:
                popupMenu.beforeSelection();
                break;
            case KeyEvent.VK_DOWN:
                popupMenu.afterSelection();
                break;
            case KeyEvent.VK_BACK_SPACE:
                search();
                break;
            case KeyEvent.VK_ENTER:
                product = (VoucherModel) popupMenu.setSelectedValue();
                btn_cek.requestFocus();
                break;
            default:
                break;
        }
    }//GEN-LAST:event_txt_voucher_idKeyPressed

