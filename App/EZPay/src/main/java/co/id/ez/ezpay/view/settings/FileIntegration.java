    private void btn_browseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_browseActionPerformed
        // TODO add your handling code here:
        fileChooser.setFileSelectionMode(javax.swing.JFileChooser.DIRECTORIES_ONLY);
        if (fileChooser.showSaveDialog(this) == javax.swing.JFileChooser.APPROVE_OPTION) {
            String tOutputFile = fileChooser.getSelectedFile().getAbsolutePath();
            txt_dbname.setText(tOutputFile);
        }
    }//GEN-LAST:event_btn_browseActionPerformed

    private void btn_simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_simpanActionPerformed
        // TODO add your handling code here:
        try {
            int opt = Common.showConfirmMessage("Simpan pengaturan?", this);
            if (opt == 0) {
                JSONObject config = new JSONObject();
                config.put("ext", ".csv")
                        .put("output", txt_dbname.getText())
                        .put("type", "file");

                if (configData != null) {

                } else {
                    appcontroller.insertIntegrationConfig(config, "FILE");
                }
            }
        } catch (SQLException e) {
            Common.showErrorMessage("Gagal menyimpan konfigurasi.\n" + e.getMessage(), this);
            Common.errorLog("[SQLException] Error inserting integration config", e);
        }
    }//GEN-LAST:event_btn_simpanActionPerformed

