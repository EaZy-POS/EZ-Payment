    @Override
    public TableActionEvent getTableActionEvent() {
        TableActionEvent event = new TableActionEvent() {
            @Override
            public void onView(int row) {
            }

            @Override
            public void onCheckStatus(int row) {
            }

            @Override
            public boolean canView() {
                return false;
            }

            @Override
            public boolean canCheck() {
                return false;
            }

            @Override
            public boolean canReprint() {
                return false;
            }

            @Override
            public void onReprint(int row) {
            }

            @Override
            public boolean canEdit() {
                return true;
            }

            @Override
            public void onEdit(int row) {
                CentralPDAMBillerModel model = (CentralPDAMBillerModel) viewTableData.getTableModel().getData().get(row);
                int opt = Common.showConfirmMessage("Rubah data biller PDAM " + model.getBiller() + "?", viewTableData);

                if (opt == 0) {
                    editData(model);
                }
            }

            @Override
            public void onDelete(int row) {
                CentralPDAMBillerModel model = (CentralPDAMBillerModel) viewTableData.getTableModel().getData().get(row);
                int opt = Common.showConfirmMessage("Hapus data biller PDAM " + model.getBiller() + "?", viewTableData);

                if (opt == 0) {
                    delete(model);
                }
            }

            @Override
            public boolean canDelete() {
                return true;
            }
        };

        return event;
    }

    @Override
    public void delete(CentralPDAMBillerModel model) {
        try {
            DeletePDAMBiller req = new DeletePDAMBiller();
            req.setId(model.getId());
            JSONObject data = sendRequetToCentral(req, "POST");
            if (data != null) {
                loadData();
                Common.showInfoMessage("Data berhasil dihapus", this);
                resetForm();
            }
        } catch (Exception e) {
            Common.showErrorMessage(MessageType.SYSTEM_ERROR, this);
            Common.errorLog("[Exception] Failed delete data jabatan", e);
        }
    }

    @Override
    public void simpan() {
        try {
            String biller = txt_biller.getText();
            String billerName = txt_nama_biller.getText();
            String district = txt_district.getText();
            String id = isEditStage ? txt_id.getText() : "";

            if (isEditStage) {
                UpdatePDAMBiller req = new UpdatePDAMBiller();
                req.setBiller(biller);
                req.setBillerName(billerName);
                req.setDistrict(district);
                req.setId(id);
                req.setStatus(cek_aktif.isSelected() ? "1" : "0");

                JSONObject data = sendRequetToCentral(req, "POST");
                if (data != null) {
                    loadData();
                    Common.showInfoMessage("Data berhasil dirubah", this);
                    resetForm();
                }
            } else {
                AddPDAMBiller req = new AddPDAMBiller();
                req.setBiller(biller);
                req.setBillerName(billerName);
                req.setDistrict(district);

                JSONObject data = sendRequetToCentral(req, "POST");
                if (data != null) {
                    loadData();
                    Common.showInfoMessage("Data berhasil disimpan", this);
                    resetForm();
                }
            }

        } catch (Exception e) {
            Common.showErrorMessage(MessageType.SYSTEM_ERROR, this);
            Common.errorLog("[SQLException] Failed insert data karyawan", e);
            resetForm();
        }
    }

    @Override
    public void editData(CentralPDAMBillerModel model) {
        txt_id.setText(model.getId());
        txt_biller.setText(model.getBiller());
        txt_nama_biller.setText(model.getBillerName());
        txt_district.setText(model.getDistrict());
        isEditStage = true;
        cek_aktif.setVisible(true);
        cek_aktif.setSelected(model.getStatus().equalsIgnoreCase("aktif"));
        txt_biller.requestFocus();
    }

    @Override
    public String getDataName() {
        return "Data Biller PDAM";
    }

