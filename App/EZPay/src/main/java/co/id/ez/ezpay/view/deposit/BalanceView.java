    private void cekSaldo() {
        BillerResponse resp = sendRequest(new GetSaldo(), "GET", true);
        if (resp != null) {
            JSONObject respObj = resp.getPayload();
            BigDecimal balance = new BigDecimal(respObj.get("balance").toString());
            txt_saldo.setText(Common.formatRupiah(balance.doubleValue()));
        }
    }
    
